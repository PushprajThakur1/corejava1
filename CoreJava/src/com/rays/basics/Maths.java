package com.rays.basics;

public class Maths {
	public static void main(String[] args) {
		System.out.println("Mathematics function");
		System.out.println("Max 6,5 " + Math.max(6, 5));
		System.out.println("Min 5,6 " + Math.min(5, 6));
		System.out.println("Absoulate3.7 " + Math.abs(3.7));
		System.out.println("Exp 10 " + Math.exp(1));
		System.out.println("Random Number " + Math.random()*100000);
		System.out.println("Square Root " + Math.sqrt(10));

	}
}
